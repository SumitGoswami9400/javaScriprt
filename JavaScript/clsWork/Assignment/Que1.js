// Logical AND (&&)
let x = 5;
let y = 10;
if (x > 0 && y > 0) {
    console.log("Both x and y are positive.");
} else {
    console.log("At least one of x or y is not positive.");
}

// Logical OR (||)
let isRaining = true;
let isSunny = false;
if (isRaining || isSunny) {
    console.log("It's either raining or sunny.");
} else {
    console.log("It's neither raining nor sunny.");
}

// Logical NOT (!)
let isLightOn = false;
if (!isLightOn) {
    console.log("The light is off.");
} else {
    console.log("The light is on.");
}







